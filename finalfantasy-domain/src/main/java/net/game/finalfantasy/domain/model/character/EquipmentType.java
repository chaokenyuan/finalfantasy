package net.game.finalfantasy.domain.model.character;

/**
 * Enumeration of equipment types in FF6
 */
public enum EquipmentType {
    WEAPON,      // Swords, daggers, spears, etc.
    SHIELD,      // Defensive equipment for off-hand
    HELMET,      // Head armor
    ARMOR,       // Body armor
    RELIC        // Accessories/relics that provide special effects
}
